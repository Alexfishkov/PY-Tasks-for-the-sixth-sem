print ("Находим все пары дружественных чисел на интервале от 0 до указанного вами K")
k = int(input("Введите ваше натуральное K => "))
mentioned = 1
for i in range (4,k):
    sum1 = 1
    for j in range (2,i//2+1):
        if i % j == 0:
            sum1 +=j
    if sum1 > 1:
        for ii in range (i,k):
            sum2 = 1
            for jj in range (2,ii//2+1):
                if ii % jj == 0:
                    sum2 +=jj
            if sum2 == i and sum1 == ii and i != ii and i != mentioned:
                print(f"{i} - {ii}")
                mentioned = ii















